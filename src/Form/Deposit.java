    // 3. TAMPIL DATA (Pastikan Urutan Kolom Tabel Sesuai)
    private void tampilData() {
        DefaultTableModel model = (DefaultTableModel) tbl_Deposit.getModel();
        model.setRowCount(0);
        kursIndo.setMaximumFractionDigits(0);

        double total = 0;
        int no = 1;

        try {
            String sql = "SELECT * FROM tbl_deposit ORDER BY id DESC";
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();

            while (rs.next()) {
                String id = rs.getString("id");
                String tgl = rs.getString("tanggal");
                String ket = rs.getString("keterangan");
                int jml = rs.getInt("jumlah");

                total += jml;

                Object[] data = {
                    no++,
                    id,    // Kolom 1 (ID)
                    tgl,   // Kolom 2 (Tanggal)
                    ket,   // Kolom 3 (Keterangan)
                    kursIndo.format(jml) // Kolom 4 (Jumlah)
                };
                model.addRow(data);
            }
            txt_Total.setText(kursIndo.format(total));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Tampil: " + e.getMessage());
        }
    }

