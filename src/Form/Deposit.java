    private void btn_SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SimpanActionPerformed
        // TODO add your handling code here:
        if (txt_Keterangan.getText().isEmpty() || txt_Jumlah.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data tidak boleh kosong!");
            return;
        }

        try {
            String sql = "INSERT INTO tbl_deposit (tanggal, keterangan, jumlah) VALUES (?, ?, ?)";
            ps = conn.prepareStatement(sql);
            
            ps.setString(1, lbl_Tanggal.getText());
            ps.setString(2, txt_Keterangan.getText());
            
            // Hapus titik/koma format rupiah sebelum simpan ke DB
            String jmlMurni = txt_Jumlah.getText().replaceAll("[^0-9]", "");
            ps.setString(3, jmlMurni);
            
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Deposit Berhasil Disimpan");
            
            tampilData();
            reset();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Simpan: " + e.getMessage());
        }
    }//GEN-LAST:event_btn_SimpanActionPerformed

    private void btn_HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_HapusActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbl_Deposit.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Pilih baris yang ingin dihapus!");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(null, "Hapus data deposit ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (confirm == 0) {
            try {
                // Ambil ID dari kolom index 1
                String id = tbl_Deposit.getValueAt(selectedRow, 1).toString();
                
                String sql = "DELETE FROM tbl_deposit WHERE id = ?";
                ps = conn.prepareStatement(sql);
                ps.setString(1, id);
                ps.executeUpdate();
                
                tampilData();
                JOptionPane.showMessageDialog(null, "Data Terhapus");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Gagal Hapus: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btn_HapusActionPerformed

