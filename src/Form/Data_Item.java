    private void formatRupiah() {
        if (!txt_Harga.getText().equals("")) {
            // Hapus semua karakter kecuali angka (0-9)
            String Replace = txt_Harga.getText().replaceAll("[^0-9]", ""); 
            double FormatRp = Double.parseDouble(Replace);
            DecimalFormat dcf = new DecimalFormat("#,###,###");
            txt_Harga.setText(dcf.format(FormatRp));
        }
}
    private void btn_SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SimpanActionPerformed
        // 1. Validasi Input Kosong
        if (txt_Nama.getText().equals("") || txt_Harga.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Data Yang Akan di Masukkan Belum Lengkap", "Validasi", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try {
            st = conn.createStatement();
            
            // Bersihkan format (hapus koma/titik) sebelum disimpan ke database
            String hargaMurni = txt_Harga.getText().replaceAll("[^\\d]", "");

            if (btn_Simpan.getText().equals("Simpan")) {
                // --- INSERT DATA ---
                String find = "SELECT * FROM tbl_dataitem WHERE nama_item='" + txt_Nama.getText() + "'";
                rs = st.executeQuery(find);
                
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "Menu Sudah Pernah di Masukkan, Silahkan Masukan Menu Lain", "Validasi", JOptionPane.WARNING_MESSAGE);
                } else {
                    String nama = txt_Nama.getText();
                    String kategori = cmb_Kategori.getItemAt(cmb_Kategori.getSelectedIndex());
                    
                    String sql = "INSERT INTO tbl_dataitem (nama_item, kategori, harga) VALUES (?, ?, ?)";
                    
                    ps = conn.prepareStatement(sql);
                    ps.setString(1, nama);
                    ps.setString(2, kategori);
                    ps.setString(3, hargaMurni); // Gunakan harga yang sudah dibersihkan
                    
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data Berhasil di Simpan", "Input Data", JOptionPane.INFORMATION_MESSAGE);
                    showdata();
                    reset();
                }
            } else {
                // Ambil nama item dari TABEL (Nama LAMA sebelum diedit)
                int selectedRow = tbl_Item.getSelectedRow();
                String namaLama = tbl_Item.getValueAt(selectedRow, 1).toString();

                // Query Update: Ubah data DI MANA nama_item = namaLama
                String updateSql = "UPDATE tbl_dataitem SET nama_item = ?, kategori = ?, harga = ? WHERE nama_item = ?";

                ps = conn.prepareStatement(updateSql);
                ps.setString(1, txt_Nama.getText()); // Nama Baru
                ps.setString(2, cmb_Kategori.getItemAt(cmb_Kategori.getSelectedIndex()).toString());
                ps.setString(3, hargaMurni);
                ps.setString(4, namaLama); // Where Nama Lama

                ps.executeUpdate();

                JOptionPane.showMessageDialog(null, "Data berhasil di-Update");

                showdata(); 
                reset();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btn_SimpanActionPerformed

    private void tbl_ItemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_ItemMouseClicked
        DefaultTableModel model = (DefaultTableModel) tbl_Item.getModel();
    int selectedRow = tbl_Item.getSelectedRow();

    txt_Nama.setText(model.getValueAt(selectedRow, 1).toString());

    // Set ComboBox
    String colomKat = model.getValueAt(selectedRow, 2).toString();
    for (int i = 0; i < cmb_Kategori.getItemCount(); i++) {
        if (cmb_Kategori.getItemAt(i).toString().equalsIgnoreCase(colomKat)) {
            cmb_Kategori.setSelectedIndex(i);
        }
    }

    // --- PERBAIKAN UTAMA ADA DI SINI ---
    String hargaTabel = model.getValueAt(selectedRow, 3).toString(); // Contoh: "Rp15.000,00"
    
    // 1. Pisahkan dulu bagian rupiah dengan sen (koma)
    String[] pisah = hargaTabel.split(","); // Memisahkan "Rp15.000" dan "00"
    
    // 2. Ambil hanya bagian depannya saja (Rp15.000)
    String hargaUtama = pisah[0]; 
    
    // 3. Bersihkan simbol Rp dan titik
    String hargaBersih = hargaUtama.replaceAll("[^0-9]", ""); // Hasil: "15000"
    
    txt_Harga.setText(hargaBersih);
    // ------------------------------------

    btn_Simpan.setText("Update");
    formatRupiah(); // Format ulang agar jadi "15.000" di textfield
    }//GEN-LAST:event_tbl_ItemMouseClicked

