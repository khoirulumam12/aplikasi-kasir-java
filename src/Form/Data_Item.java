    private void btn_HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_HapusActionPerformed
        try {
        if (txt_Nama.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Silakan pilih data yang akan dihapus", "Validasi", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        int konfirmasi = JOptionPane.showConfirmDialog(null, "Apakah Anda ingin menghapus data ini?", "Hapus Data", JOptionPane.YES_NO_OPTION);

        if (konfirmasi == 0) { 
            // PERUBAHAN DI SINI:
            // Kita hapus berdasarkan NAMA ITEM (Kolom indeks 1), bukan Nomor
            DefaultTableModel model = (DefaultTableModel) tbl_Item.getModel();
            int selectedRow = tbl_Item.getSelectedRow();
            
            // Ambil Nama Item dari tabel (Kolom ke-1)
            String namaItem = model.getValueAt(selectedRow, 1).toString();

            // Query Delete menggunakan WHERE nama_item
            String sql = "DELETE FROM tbl_dataitem WHERE nama_item = ?";
            ps = conn.prepareStatement(sql);
            ps.setString(1, namaItem); // Set parameter berdasarkan nama
            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data berhasil dihapus");

            showdata(); // Refresh tabel
            reset();    
        } else {
            reset(); 
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
    }//GEN-LAST:event_btn_HapusActionPerformed

