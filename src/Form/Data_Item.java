    private void showdata() {
        DefaultTableModel model = (DefaultTableModel) tbl_Item.getModel();
        model.setRowCount(0);

        // Setup Format Rupiah (Opsional, sesuai kode sebelumnya)
        java.text.NumberFormat kursIndo = java.text.NumberFormat.getCurrencyInstance(new java.util.Locale("id", "ID"));
        kursIndo.setMaximumFractionDigits(0); 

        try {
            String sql = "SELECT * FROM tbl_dataitem";
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();

            // BUAT VARIABEL NO URUT BARU DI SINI
            int noUrut = 1; 

            while (rs.next()) {
                // Kita tidak pakai rs.getInt("No") untuk tampilan
                String nama = rs.getString("nama_item");
                String kategori = rs.getString("kategori");
                int harga = rs.getInt("harga");

                // Format Harga
                String hargaFormatted = kursIndo.format(harga);

                // Masukkan noUrut ke kolom pertama, lalu noUrut ditambah 1 (noUrut++)
                Object[] rowData = {noUrut++, nama, kategori, hargaFormatted};
                model.addRow(rowData);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

